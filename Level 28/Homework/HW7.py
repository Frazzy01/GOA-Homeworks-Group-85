# დაბეჭდე სიტყვებით, მაგალითად: 1 → One 2 → Two გამოიყენე if / elif / else

# მომხმარებლისგან რიცხვის შეყვანა
number = int(input("sheiyvanet marto 1-5-mde: "))

if number == 1:
    print("One")
elif number == 2:
    print("Two")
elif number == 3:
    print("Three")
elif number == 4:
    print("Four")
elif number == 5:
    print("Five")
else:
    print("shemoiyvanet ricxvi mxolod 1–5 shoris")