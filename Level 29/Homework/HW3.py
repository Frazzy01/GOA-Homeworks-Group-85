# შენი დავალებაა დაითვალო თუ რამდენი ცალი რიცხვი გვხვდება სიაში რომელიც არის 50 ზე მეტი

numbers = [10, 55, 72, 30, 90, 45, 60]

i = 0
count = 0

while i < len(numbers):
    if numbers[i] > 50:
        count += 1
    i += 1

print("50 ze meti ricxvebi:", count)